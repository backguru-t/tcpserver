def create_normal_data(header, channel, data):
    # 시퀀스 종료 바이트
    end_of_sequence = [0x03, 0x0d, 0x0a]
    
    # 바이트 배열 생성
    normal_data = bytearray(header + [channel] + data + end_of_sequence)
    
    return normal_data

def format_byte_array(byte_array):
    # 헤더 출력
    header_str = ', '.join(f'0x{byte:02x}' for byte in byte_array[:6]) + ','
    
    # 채널 번호 출력
    channel_str = f'0x{byte_array[6]:02x},'
    
    # 실 데이터 출력 (한 행에 16개씩)
    data_str = '\n'.join(', '.join(f'0x{byte:02x}' for byte in byte_array[i:i+16]) + ',' for i in range(7, len(byte_array) - 3, 16))
    
    # 시퀀스 종료 바이트 출력
    end_str = ', '.join(f'0x{byte:02x}' for byte in byte_array[-3:]) 
    
    return f"{header_str}\n{channel_str}\n{data_str}\n{end_str}"

